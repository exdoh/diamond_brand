diamond_brand.prototype.click_active = function(name){
	if(name == "home")
	{
		$('#banner').css('background-size','100% 100%');
		
		$('#homepage').show();
		$('#carousel').show();
		$('#productspage').hide();
		$('#aboutpage').hide();
		
		$('#home').attr('class','font-footer active');
		$('#products').attr('class','font-footer dropdown');
		$('#aboutcontact').attr('class','font-footer');
	} 
	else if (name == "products") 
	{
		/*$('#homepage').hide();
		$('#carousel').hide();
		$('#productspage').show();
		$('#aboutpage').hide();*/
		
		$('#home').attr('class','font-footer');
		//$('#products').attr('class','dropdown active');
		$('#aboutcontact').attr('class','font-footer');
		
	}
	else if (name == "aboutcontact") 
	{
		$('#banner').css('background-size','100% auto');
		
		$('#homepage').hide();
		$('#carousel').hide();
		$('#productspage').hide();
		$('#aboutpage').show();
		
		$('#home').attr('class','font-footer');
		$('#products').attr('class','font-footer dropdown');
		$('#aboutcontact').attr('class','font-footer active');
	}
	else {
	    $('#banner').css('background-size','100% auto');
	    
		$('#homepage').hide();
		$('#carousel').hide();
		$('#productspage').show();
		$('#aboutpage').hide();
		
		$('#home').attr('class','font-footer');
		$('#products').attr('class','font-footer dropdown active');
		$('#aboutcontact').attr('class','font-footer');
		
		$('html,body').animate({
        	scrollTop: $("#"+name).offset().top-80},
        2000);
	}
	
}

